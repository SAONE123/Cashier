const maxLength = Math.min(image.length, judul.length, harga.length);

function generateCSV(images, titles, prices) {
  const headers = ["Image", "Title", "Price"];
  const rows = [];

  for (let i = 0; i < maxLength; i++) {
    rows.push([images[i], titles[i], prices[i]]);
  }

  const csvContent = [
    headers.join(","),
    ...rows.map((row) => row.join(",")),
  ].join("\n");

  return `data:text/csv;charset=utf-8,${encodeURIComponent(csvContent)}`;
}

